package javacoding;

public class ParentClass {

	public ParentClass() {
		System.out.println("This is a parent class constructor");
	}

	public ParentClass(int a) {
		System.out.println("This is a parent class constructor with value of a: " + a);
	}

	public ParentClass(int a, int b) {
		System.out.println("This is a parent class constructor with value of a and b " + a + " " + b);

	}

}
